



function showImgA() {
    let imageA = document.querySelector(".imageA");
    imageA.style.visibility = "visible";
    let hide_aimg = document.querySelector(".hide_aimg");
    hide_aimg.style.visibility = "visible";
}

function hideImgA() {
    let imageA = document.querySelector(".imageA");
    imageA.style.visibility = "hidden";
    let hide_aimg = document.querySelector(".hide_aimg");
    hide_aimg.style.visibility = "hidden";

}

